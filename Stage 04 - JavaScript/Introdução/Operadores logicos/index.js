/* 
  operadores logicos (logical operators)

  valores booleanos, quando verificados,
  resultara em verdadeiro ou falso

*/

let pao = true
let queijo = false

// AND -> &&
/* se um dos dois forem falsos, retornara falso
  necessario os dois serem verdadeiros

  console.log(pao && queijo)
*/


// OR -> ||
/* se um dos dois forem verdadeiro, retornara, verdadeiro

  console.log(pao || queijo)
*/

// NOT -> !
/* operador de negação, sempre que usado numa variavel
  negara o valor dela
*/

